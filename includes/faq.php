<script>
    document.addEventListener('DOMContentLoaded', () => {
        const toggleButton = document.querySelector('.faq-toggle');
        const faqItems = document.querySelectorAll('.faq-item');
        const hiddenFaqItems = document.querySelectorAll('.faq-hidden');
        const initialVisibleCount = 5;

        // Show only the initialVisibleCount items
        faqItems.forEach((item, index) => {
            if (index >= initialVisibleCount) {
                item.classList.add('faq-hidden');
            }
        });

        toggleButton.addEventListener('click', (e) => {
            e.preventDefault();
            const isShowingAll = toggleButton.textContent === 'See less';

            if (isShowingAll) {
                hiddenFaqItems.forEach(item => item.classList.add('faq-hidden'));
                toggleButton.textContent = 'See all';
            } else {
                hiddenFaqItems.forEach(item => item.classList.remove('faq-hidden'));
                toggleButton.textContent = 'See less';
            }
        });
    });
</script>